% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%     url='http://valdemar.kms.dk/trf/';
%     str=webread(url)
%     web(url)
    commandStr = 'python /Users/xiaohu/.spyder/temp.py';
    
    x=str2double(get(handles.edit3,'String'));
    y=str2double(get(handles.edit4,'String'));
    z=str2double(get(handles.edit5,'String'));
    
    commandStr = strcat(commandStr,' --x=', num2str(x), ' --y=', num2str(y), ' --z=', num2str(z));
    
    [status, commandOut] = system(commandStr);
    if status == 0
        values = [];
        str = '';
        for i = 1:1:numel(commandOut)
            if commandOut(i) >= '0' && commandOut(i) <= '9' || commandOut(i) == '.'
                str = strcat(str,commandOut(i));
            else
                values = [values;str2double(str)];
                str = '';
            end
        end
        if numel(values) == 3
            set(handles.edit20,'String',num2str(values(1)));
            set(handles.edit21,'String',num2str(values(2)));
            set(handles.edit22,'String',num2str(values(3)));
        else
            disp('Error: web check failure.')
        end
    else
        disp('Error: web check failure.');
    end


